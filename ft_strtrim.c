#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*ptr;
	size_t	start;
	size_t	end;

	if (!s1)
		return (0);
	if (!set)
		return (ft_strdup(s1));
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	ptr = (char *) malloc((end - start + 1) * sizeof(char));
	if (!ptr)
		return (0);
	ft_strlcpy(ptr, s1 + start, end - start + 1);
	ptr[end - start] = '\0';
	return (ptr);
}
